                                        <p>
                                            The world which we live in is bombarded with climate change related calamities to natural disasters, pandemics to man-made disasters, these situations have impacted millions of people worldwide directly or indirectly. Being a globalized and connected world, each and every disaster event has a bearing on the world. As the disasters are inevitable, the next best we can do is to prepare for all the worst case scenarios. In the case of worst case scenarios, responses need to be quick and efficient, the resources have to be scrambled from multiple locations to various other locations. Time will be of the essence to reach victims and other critical areas of vulnerability such as power stations and dams. GIS is the technology which can help with the various activities associated with emergency response.
                                        </p>
                                        <h3>Preparedness</h3>
                                        <p>
                                            Authorities in power need to develop action plans which can be an effective response to an emergency. With GIS mapping capabilities, authorities map out routes from and to critical locations. Using historical data agencies now can identify areas which are prone to specific kinds of disasters on a regular basis. With GIS, authorities can prepare for emergencies, they can identify vulnerable areas with people and critical infrastructures. New and efficient emergency evacuations can be drawn up with the new information from GIS-based models. New sites for emergency relief camps can be chosen as well.
                                        </p>
                                        <h3>Field Operations</h3>
                                        <p>In any case of relief operations, one of the most important things is the movement of resources between points of refilling stations. For this, roads which are not affected by disasters or even new paths have to be known. This is where GIS can help emergency personnel setup relief and operations camps which are accessible to multiple types of emergency personnel.

                                        </p>
                                        <h3>Response</h3>
                                        <p>Set up camps and relief centres which are connected to unaffected areas, make sure that there logistics support and mediums of logistics which are unaffected by the natural disasters. Map out a path for future reinforcements and resource replenishments as well, with the GIS advantage.

                                        </p>
                                        <h3>Data Management</h3>
                                        <p>The data from the previous natural disasters can be added to the GIS solution to plan out for disasters and be better prepared for the worst. Future infrastructure planning can factor in floods and earthquakes as well. Cities can be planned with multiple logistical outlets so as to have multiple entry and exit points from and to the city in case of a natural disaster. The solution can be scaled up to cover cities, states and countries as well. 

                                        </p>
                                        <h3>Recovery</h3>
                                        <p>GIS assists in recovery efforts by restoring the affected area to normal. This includes making decisions that address short-term and long-term requirements; such as medical care, shelter, food, rebuilding, reemployment, and the repair of essential infrastructure. GIS also plays an important role in the assessment of damage and losses to further identify priorities for reconstruction and determine actions for various field operations such as first aid and health, transportation routes.

                                        </p>
                                        <p>GIS nowadays plays a vital role in many different aspects of our daily life, they are used by city planners, police departments, health departments and more. GIS technology allows large agencies to forecast risk, planning, situational analysis and more. </p>



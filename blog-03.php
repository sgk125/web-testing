                                        <p>
                                            As the world comes to a stage where we are thinking between different vaccines for the pandemic, the effects of the pandemic will linger, GIS helped all the pandemic response professionals by tracking the spread of the disease. GIS came into the picture
                                            soon as the illness began its evolution from an epidemic to endemic to the full-blown epidemic as we know it today. GIS-based Covid19 dashboards began popping up to track and inform the general public about
                                            the spread of the disease. More than to track the disease, GIS allowed health officials to identify clusters, inform the general public about treatment centres, display specific geographical locations under
                                            lockdown and more.
                                        </p>
                                        <h3>Why GIS?</h3>
                                        <h5>Collective Spatial Data Management</h5>
                                        <p>
                                            A pandemic has too much information, such as name, personal details, location, disease, stages of the disease, the timeline of the infection to quarantine and more. With a geographical context, GIS can display information on a real-time and scalable method.
                                            The pandemic has affected the common man on a local to a global scale, implementation of GIS has followed patterns, from dashboards showing global information to the dashboard of a small area. From the proximity
                                            of local areas to far-reaching continents, this shows the ability of the GIS to adapt to any scale of information processing and information output. GIS can also integrate and process real-time information such
                                            as surveillance activities and data management activities. Adding information such as their socioeconomic and educational standing gives a better idea to Covid19 responders to better approach specific populations.
                                        </p>
                                        <h5>Visualization</h5>
                                        <p>The data visualization abilities of GIS helps to overcome the limitations of complex data when fetched individually or connected with any other metric. Another advantage of GIS’s data visualization is its ability
                                            to display large chunks of data with any dialect, thus removing any errors in translation. Additional features can be added such as distances between primary care centres, testing centres, quarantine centres
                                            and hospitals. Tracking and calculating these distances can give the first responders the ability to provide the best help to all patients. Charting the spread of the disease with algorithms, animations can
                                            give the health officials the predictive ability to better prepare for the immediate future.
                                        </p>
                                        <h5>Spatial Statistical Modelling</h5>
                                        <p>Kriging is the go-to method used for integrating and interoperating health data into other metrics. Kriging has become perhaps the best embedded into proprietary GIS. Here, an attempt is made to model the value
                                            of locations where diseases and the number of infections have not been sampled. This describes the way the pattern of spatial dependence declines with distance or spatial lag. This allows epidemiology specialists
                                            and public health officials to identify space-time “clusters” or "hot spots" of disease.
                                        </p>
                                        <h5>Spatial Interaction Model</h5>
                                        <p>
                                            With a spatial interaction model, public health officials can predict the spread of disease with analyzing the movement of people, movement of goods, movement of information and more. This flow of metrics between rural, urban and countries are types of
                                            spatial interaction that are central to disease transmission and by modelling these individual flows, public health can predict and identify the spread of disease with higher accuracy.
                                        </p>
                                        <h5>GIS For Health</h5>
                                        <p>Every disease is classified concerning epidemiology, it is the study and analysis of the distribution (who, when, and where), patterns and determinants of health and disease conditions in defined populations (from
                                            Wikipedia). GIS can connect unseemly dots between metrics and show the information in an easy to digest format. GIS was used during the Ebola outbreak to successfully cull the disease beyond the nation’s borders,
                                            barring few exceptions. In fact, John Snow the father of epidemiology was the first to chart clusters of cholera cases in the London epidemic of 1854 on a physical map. GIS or its primitive iterations historically
                                            proved its mettle to track diseases, in the case of Covid19, we will be tracking the vaccination of the illness too.

                                        </p>


